package Java_Basic.OperadoresyVariables;

public class OperadoresAritmeticos

{

    public static void main(String[] args)
    {
        //Suma
        int a = 30, b = 5;
        var resultado = a + b;
        System.out.println("La Suma es: " + resultado);

        //multiplicacion
        int z = 12, y =52;
        var prod = z * y;
        System.out.println("La Multiplicacion es: " + prod);


        //Division
        var division = 50/2;
        System.out.println("El resultado de la division 1 es: " + division);
        double datosDivisionDos = a / z;
        System.out.println("El resultado de la division 2 es: "+ datosDivisionDos);


    }

}
